package com.TfgBeaEst;

import java.util.Date;

public class Animales {

	public String NumIdentificacion;
	public String Sexo;
	public Integer AnoNacimiento;
	public String Usuario;
	public Boolean Muerta;
	public Date FechaMuerte;
	public Boolean Venta;
	public Date FechaVenta;
	public Boolean TieneBolo;
	public Boolean TieneCrotal;
	public String NumExplotacion;
	
	public String getNumIdentificacion() {
		return NumIdentificacion;
	}
	
	public void setNumIdentificacion(String numIdentificacion) {
		NumIdentificacion = numIdentificacion;
	}
	
	public String getSexo() {
		return Sexo;
	}
	
	public void setSexo(String sexo) {
		Sexo = sexo;
	}
	
	public Integer getAnoNacimiento() {
		return AnoNacimiento;
	}
	
	public void setAnoNacimiento(Integer anoNacimiento) {
		AnoNacimiento = anoNacimiento;
	}
	
	public String getUsuario() {
		return Usuario;
	}
	
	public void setUsuario(String usuario) {
		Usuario = usuario;
	}
	
	public Boolean getMuerta() {
		return Muerta;
	}
	
	public void setMuerta(Boolean muerta) {
		Muerta = muerta;
	}
	
	public Date getFechaMuerte() {
		return FechaMuerte;
	}
	
	public void setFechaMuerte(Date fechaMuerte) {
		FechaMuerte = fechaMuerte;
	}
	
	public Boolean getVenta() {
		return Venta;
	}
	
	public void setVenta(Boolean venta) {
		Venta = venta;
	}
	
	public Date getFechaVenta() {
		return FechaVenta;
	}
	
	public void setFechaVenta(Date fechaVenta) {
		FechaVenta = fechaVenta;
	}
	
	public Boolean getTieneBolo() {
		return TieneBolo;
	}
	
	public void setTieneBolo(Boolean tieneBolo) {
		TieneBolo = tieneBolo;
	}
	
	public Boolean getTieneCrotal() {
		return TieneCrotal;
	}
	
	public void setTieneCrotal(Boolean tieneCrotal) {
		TieneCrotal = tieneCrotal;
	}
	
	public String getNumExplotacion() {
		return NumExplotacion;
	}
	
	public void setNumExplotacion(String numExplotacion) {
		NumExplotacion = numExplotacion;
	}
	
}
